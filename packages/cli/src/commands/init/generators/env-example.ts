import { join } from "node:path";

import { fileExists, readTextFile, writeTextFile } from "../../../shared/fs";
import type { AuthProvider, DatabaseProvider, GeneratorResult, InitContext } from "../types";

// ── Env var lookup tables ─────────────────────────────────────────────────────

