  mysql: ["DATABASE_URL=mysql://user:password@localhost:3306/mydb"],
  mariadb: ["DATABASE_URL=mysql://user:password@localhost:3306/mydb"],
  sqlite: ["DATABASE_URL=file:./mydb.sqlite"],
  mongodb: ["MONGODB_URI=mongodb://localhost:27017/mydb"],
  supabase: [
    "SUPABASE_URL=https://your-project.supabase.co",
    "SUPABASE_ANON_KEY=your-anon-key",
    "SUPABASE_SERVICE_KEY=your-service-key",
  ],
  neon: [
    "DATABASE_URL=postgresql://user:password@ep-xxx.us-east-2.aws.neon.tech/mydb?sslmode=require",
  ],
  firebase: [
    "FIREBASE_PROJECT_ID=your-project-id",
    "FIREBASE_PRIVATE_KEY=your-private-key",
    "FIREBASE_CLIENT_EMAIL=service@your-project.iam.gserviceaccount.com",
  ],
