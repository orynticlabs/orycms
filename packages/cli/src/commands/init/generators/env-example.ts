const ORYCMS_SENTINEL = "# OryCMS";

// ── Content builder ───────────────────────────────────────────────────────────

export function buildEnvExample(ctx: InitContext): string {
  const { answers } = ctx;
  const lines: string[] = [`# OryCMS environment variables — generated by \`orycms init\``];

  lines.push("", `# Database (${answers.database})`, ...DB_VARS[answers.database]);

  const authVars = AUTH_VARS[answers.auth];
  if (authVars.length > 0) {
    lines.push("", `# Auth (${answers.auth})`, ...authVars);
  }

  lines.push("", "# OryCMS", "ORYCMS_DATABASE_URL=${DATABASE_URL}");

  return lines.join("\n") + "\n";
}

/**
 * Merge new env vars into an existing .env.example without overwriting lines
 * that are already present (idempotent).
 */
export function mergeEnvExample(existing: string, addition: string): string {
  const existingLines = new Set(existing.split("\n").map((l) => l.trim()));
  const newLines: string[] = [];

  for (const line of addition.split("\n")) {
    if (!existingLines.has(line.trim())) {
      newLines.push(line);
    }
  }

  if (newLines.every((l) => l.trim() === "" || l.startsWith("#"))) {
    return existing; // nothing meaningful to add
  }

  return existing.trimEnd() + "\n\n" + newLines.join("\n") + "\n";
}

// ── Idempotent runner ─────────────────────────────────────────────────────────

export function generateEnvExample(ctx: InitContext): GeneratorResult {
  const path = ".env.example";
  const full = join(ctx.cwd, path);
  const content = buildEnvExample(ctx);

  if (!fileExists(full)) {
    writeTextFile(full, content);
    return { path, status: "created" };
  }

  const existing = readTextFile(full);
  if (existing.includes(ORYCMS_SENTINEL)) {
    return { path, status: "skipped", description: ".env.example already contains OryCMS vars" };
  }

  writeTextFile(full, mergeEnvExample(existing, content));
  return { path, status: "updated" };
}
